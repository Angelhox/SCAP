    void guardar() {
        if (txtCodigo.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el código del implemento");
            txtCodigo.requestFocus();
            return;
        }
        if (txtNombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el nombre del implemento");
            txtNombre.requestFocus();
            return;
        }
        if (txtTipo.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el tipo de implemento");
            txtTipo.requestFocus();
            return;
        }
        if (txtDescripcion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar la descripçion del implemento");
            txtDescripcion.requestFocus();
            return;
        }
        if (txtMarca.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar la marca del implemento");
            txtMarca.requestFocus();
            return;
        }

        if (txtPrecio.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el precio del implemento");
            txtPrecio.requestFocus();
            return;
        }
        if (txtIva.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el iva del implemento");
            txtPrecio.requestFocus();
            return;
        }

        if (txtStock.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el stock con del implemento");
            txtStock.requestFocus();
            return;
        }

        clsImplementos dts = new clsImplementos();
        fImplementos funcImplementos = new fImplementos();

        dts.setCodigo(txtCodigo.getText());
        dts.setNombre(txtNombre.getText());
        dts.setTipo(txtTipo.getText());
        dts.setDescripcion(txtDescripcion.getText());
        dts.setMarca(txtMarca.getText());
        dts.setStock(Double.parseDouble(txtStock.getText()));
        dts.setPrecio(Double.parseDouble(txtPrecio.getText()));
        dts.setIva((Double.parseDouble(txtIva.getText())) / 100);
        dts.setFkInventario(Integer.parseInt(txtFkInventario.getText()));

        if (accion.equals("nuevo")) {
            try {
                if (funcImplementos.insertar(dts)) {
                    JOptionPane.showConfirmDialog(rootPane, "El implemento se ha registrado satisfactoriamente");
                    cargarTabla("", "Codigo", "Codigo", "desc");
                    limpiarCampos();
                    isActiveEditar = false;
                    isActiveNuevo = false;
                    inhabilitar();
                }
            } catch (Exception e) {
                JOptionPane.showConfirmDialog(rootPane, "Ha ocurrido un error " + e);

            }
        } else if (accion.equals("editar")) {
            try {
                dts.setId(Integer.parseInt(txtId.getText()));
                if (funcImplementos.editar(dts)) {
                    JOptionPane.showConfirmDialog(rootPane, "Se ha actualizado el registro del implemento " + dts.getMarca() + " " + dts.getCodigo());
                    cargarTabla("", "Codigo", "Codigo", "desc");
                    isActiveEditar = false;
                    isActiveNuevo = false;
                    inhabilitar();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Ha ocurrido un error al actualizar el registro", "Error", JOptionPane.INFORMATION_MESSAGE, null);
            }
        } else if (accion.equals("null")) {
            JOptionPane.showMessageDialog(null, "Seleciona la opcion de actualizar registro primero", "Error", JOptionPane.INFORMATION_MESSAGE, null);

        } else {
            JOptionPane.showMessageDialog(null, "No se ha entendido  tu peticion", "Error", JOptionPane.INFORMATION_MESSAGE, null);
        }

    }
